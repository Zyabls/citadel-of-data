        <p>В этой игре вам предстоит защищать системы от атак, используя различные средства защиты информации. Игроки будут выполнять задания, реагировать на угрозы и зарабатывать баллы.</p>
        
        <h3>Цель игры</h3>
        <p>Основная цель игры – научиться выстраивать эффективную защиту от возможных киберугроз, развивая навыки и умения сотрудников информационной безопасности.</p>
        
        <h3>Режимы игры</h3>
        <ul>
            <li><strong>Обычный режим:</strong> команды приобретают СЗИ, получают риски с картами действия и реагируют на инциденты.</li>
            <li><strong>Усложненный режим:</strong> увеличено количество раундов и рисков, сокращается время на реагирование.</li>
        </ul>

        <p>Игроки объединяются в группы, чтобы спроектировать надёжную систему защиты предприятия в небезопасном цифровом пространстве. В их распоряжении ограниченные ресурсы и коллеги, которые могут быть не самыми быстрыми, но явно знающими своё дело. В то же время, противостоящая группа специалистов готова использовать любой шанс для атаки. Помимо конкуренции с другими командами, также стоит помнить о внешних угрозах, таких как организованный компьютерный терроризм.</p>

        <p class="ps"><strong>P.S.</strong> Пока вы читаете, соперники уже дочитали и начали действовать...</p>

