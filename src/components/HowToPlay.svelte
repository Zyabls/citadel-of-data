        <ul>
            <li><strong>Роли и предприятие:</strong> Игроки вытягивают роли и делятся по командам. В каждой команде не должно быть одинаковых сотрудников (профессий). Командир выбирает управленческую роль.</li>
            <li><strong>Карты действий:</strong> Команды получают карты действий, которые также могут быть получены за счёт рисков.</li>
        </ul>

        <h3>Ход игры:</h3>
        <p>Игра состоит минимум из 3 раундов, в каждом раунде проходят следующие этапы:</p>
        <ul>
            <li>Получение денег.</li>
            <li>Получение рисков.</li>
            <li>Приобретение СЗИ.</li>
            <li>Инциденты.</li>
        </ul>

        <h3>Ресурсы:</h3>
        <ul>
            <li><strong>Деньги:</strong> Начальный капитал – 70 цит. Доход в следующем раунде – 35 цит, меняется в зависимости от репутации.</li>
            <li><strong>Репутация:</strong> Репутация влияет на доход и итоговые баллы. Если репутация становится 0, компания выбывает.</li>
        </ul>

        <h3>Карты и Риски:</h3>
        <ul>
            <li><strong>Риски:</strong> Команды получают риски, которые могут быть положительными, отрицательными или специальными.</li>
            <li><strong>Роли:</strong> Роли делятся на ИБ, ИТ, сетевиков и директора.</li>
            <li><strong>СЗИ:</strong> Игроки могут приобретать СЗИ, принадлежащие их профессии.</li>
        </ul>

        <h3>Инциденты:</h3>
        <p>Каждая команда должна реагировать на инциденты, получая оценки за эффективность защиты. Время на реагирование – 5 минут.</p>

        <h3>Оценка защиты:</h3>
        <p>Если команда не справляется с защитой, она выплачивает штраф, равный недостающим баллам.</p>

        <h3>Кредит:</h3>
        <p>Команды могут взять кредит на 10 или 15 цит, который нужно вернуть в следующем раунде или с процентами через 1 раунд.</p>

        <h3>Дополнительные раунды и глобальные события:</h3>
        <ul>
            <li>Механики "ИТ блиц", "Факты", "Что это такое?" и "Ложная тревога".</li>
            <li>Глобальные события, такие как экономический кризис или блокировка РКН, могут затронуть все команды одновременно.</li>
        </ul>

        <h3>Условия победы:</h3>
        <p>После последнего раунда команды подсчитывают баллы. Побеждает команда с наибольшим количеством баллов.</p>

        <h3>Дополнительные правила и рекомендации:</h3>
        <ul>
            <li>При риске на уменьшение СЗИ, команда может использовать СЗИ только в количестве, больше или равном указанному на карте.</li>
            <li>Отрицательные риски реализуются сразу, положительные – в течение текущего или следующего раунда.</li>
        </ul>

        <h3>Сайт игры</h3>
        <p>На сайте игры доступны дополнительные материалы, статьи и другая информация, полезная для игроков.</p>

        <p class="ps"><strong>PS.</strong> Спасибо всем, кто поддерживает и помогает в создании этой игры!</p>
